#!/bin/bash

read -p "Enter number" Num
case $Num in
   "1")
      echo "One"
      ;;
   "2")
      echo "two"
      ;;
   "3")
      echo "Three"
      ;;
   "4")
      echo "four"
      ;;
   "5")
      echo "five"
      ;;
   "6")
      echo "six"
      ;;
   "7")
      echo "seven"
      ;;
   "8")
      echo "eight"
      ;;
   "9")
      echo "nine"
      ;;
   *)
      echo "zero"
      ;;
esac
